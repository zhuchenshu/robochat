package com.robot.chat.dto;

public class Artist {
    /**
     * 歌手名
     */
    private String name;
    /**
     * 歌手id
     */
    private Integer id;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }
}
